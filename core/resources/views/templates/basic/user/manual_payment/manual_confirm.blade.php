         function showWaitClock(target, mm = 'mm', ss = 'ss') {
             const distance = target - new Date().getTime();
             const mins = distance < 0 ? 0: Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
             const secs = distance < 0 ? 0: Math.floor((distance % (1000 * 60)) / 1000);
         
             // Output the results
             document.getElementById(mm).innerHTML = mins;
             document.getElementById(ss).innerHTML = secs;
         }
         
